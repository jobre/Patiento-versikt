using System;
using System.Collections.Generic;
using System.Text;

namespace Ortoped.Definitions
{
  public class PurchaseDefenitions
  {
    private string mIO_No;
    private string mSupplierNo;
    private string mSupplierName;
    private string mIO_Status;
    private string mCustomerOrderNo;

    public PurchaseDefenitions()
    {

    }

    public string IO_No
    {
      get { return mIO_No; }
      set { mIO_No = value; }
    }

    public string SupplierNo
    {
      get { return mSupplierNo; }
      set { mSupplierNo = value; }
    }

    public string SupplierName
    {
      get { return mSupplierName; }
      set { mSupplierName = value; }
    }

    public string IO_Status
    {
      get { return mIO_Status; }
      set { mIO_Status = value; }
    }

    public string CustomerOrderNo
    {
      get { return mCustomerOrderNo; }
      set { mCustomerOrderNo = value; }
    }
  }
}
